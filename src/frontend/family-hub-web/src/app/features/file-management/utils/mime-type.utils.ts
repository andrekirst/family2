export type FileCategory = 'image' | 'video' | 'audio' | 'document' | 'archive' | 'other';

const CATEGORY_MAP: Record<string, FileCategory> = {
  'image/': 'image',
  'video/': 'video',
  'audio/': 'audio',
  'application/pdf': 'document',
  'application/msword': 'document',
  'application/vnd.openxmlformats-officedocument': 'document',
  'application/vnd.ms-excel': 'document',
  'application/vnd.ms-powerpoint': 'document',
  'text/': 'document',
  'application/zip': 'archive',
  'application/x-rar': 'archive',
  'application/gzip': 'archive',
  'application/x-tar': 'archive',
  'application/x-7z-compressed': 'archive',
};

export function getFileCategory(mimeType: string): FileCategory {
  for (const [prefix, category] of Object.entries(CATEGORY_MAP)) {
    if (mimeType.startsWith(prefix)) return category;
  }
  return 'other';
}

